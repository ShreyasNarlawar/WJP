import jakarta.servlet.http.HttpServletResponse;
import java.io.IOException;
import java.util.List;

import com.demo.beans.Accounts;
import com.demo.service.*;


@WebServlet("/displayall")
public class DisplayAllServlet extends HttpServlet {
	private static final long serialVersionUID = 1L;

	protected void doGet(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {
		
		AccService aserv = new AccServiceImpl();
		List<Accounts> alist = aserv.displayAll();
		
		RequestDispatcher rd = request.getRequestDispatcher("displayprods.jsp");
		rd.forward(request, response);
	}

	protected void doPost(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {
		// TODO Auto-generated method stub
		doGet(request, response);
	}

}
